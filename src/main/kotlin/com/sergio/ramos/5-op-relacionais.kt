package com.sergio.ramos

fun main() {
    var valor1 = 20
    var valor2 = 10

    // false
    println(valor1 == valor2)

    // true
    println(valor1 != valor2)

    // true
    println(valor1 > valor2)

    // false
    println(valor1 < valor2)

    // true
    println(valor1 >= 20)

    // false
    println(valor1 > 20)

    // true
    println(valor1 <= 20)

    // false
    println(valor1 < 20)
}