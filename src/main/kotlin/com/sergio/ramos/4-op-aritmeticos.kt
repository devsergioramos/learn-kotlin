package com.sergio.ramos

fun main() {
    var numero1 = 20
    var numero2 = 8

    // soma
    println(numero1 + numero2)

    // sub
    println(numero1 - numero2)

    // div
    println(numero1 / numero2)

    // mult
    println(numero1 * numero2)

    // mod
    println(numero1 % numero2)

    // incr
    println(++numero1)

    // decr
    println(--numero2)
}